let name = "Akash";
let mark = 90;

if (mark >= 90) {
    console.log(name, "Grade: A");
} else if (mark >= 75) {
    console.log(name, "Grade: B");
} else if (mark >= 60) {
    console.log(name, "Grade: C");
} else if (mark >= 50) {
    console.log(name, "Grade: D");
} else {
    console.log(name, "Grade: F");
}